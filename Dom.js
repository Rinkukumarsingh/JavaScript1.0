//  ## DOM In JavaScript

// Window Object in JS
// The window object is the global object in JavaScript, it represents the open window in the browser and it is the top-level object in the JavaScript hierarchy.
// It provides access to the browser's features and allows us to interact with the browser window, such as opening new windows, displaying alerts, etc.

// Document Object in JS
// The document object is a property of the window object, it represents the HTML document that is loaded in the browser window.
// It provides access to the elements of the HTML document and allows us to manipulate the content and structure of the document using JavaScript.

//hierarchy of window and document object in JS
// window
//  |
//  |-- document
//       |
//       |-- HTML elements (like div, p, h1, etc.)


// Basically DOM is a way to access and manipulate the HTML document using JavaScript. 
// It allows us to change the content, structure and style of the HTML document dynamically, without reloading the page.

